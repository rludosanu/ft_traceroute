#include "ft_traceroute.h"

int		ft_isnumber(char *s)
{
	if (s == NULL)
		return (0);
	while (*s != '\0')
	{
		if (*s < '0' || *s > '9')
			return (0);
		s++;
	}
	return (1);
}
